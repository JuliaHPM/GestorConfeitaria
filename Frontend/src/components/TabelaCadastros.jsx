import * as React from 'react';
import { DataGrid, GridOverlay, gridPageCountSelector, gridPageSelector, useGridApiContext, useGridSelector, } from '@mui/x-data-grid';
import { Box, LinearProgress, Pagination, PaginationItem } from '@mui/material';
import { pink } from '@mui/material/colors';

function CustomLoadingOverlay() {
    return (
        <GridOverlay>
            <div style={{ position: 'absolute', top: 0, width: '100%' }}>
                <LinearProgress />
            </div>
        </GridOverlay>
    );
}

function CustomPagination() {
    const apiRef = useGridApiContext();
    const page = useGridSelector(apiRef, gridPageSelector);
    const pageCount = useGridSelector(apiRef, gridPageCountSelector);

    return (
        <Pagination
            color="secondary"
            variant="outlined"
            shape="rounded"
            page={page + 1}
            count={pageCount}
            // @ts-expect-error
            renderItem={(props2) => <PaginationItem {...props2} disableRipple />}
            onChange={(event, value) => apiRef.current.setPage(value - 1)}
        />
    );
}

export default function DataTable({ rows, columns, width, height, serviceForDelete, loading }) {

    return (
        <>
            <Box
                sx={{
                    height: height ? height : 540,
                    width: width,
                    // margin: 'auto',
                    '& .actions': {
                        color: 'text.secondary',
                    },
                    '& .textPrimary': {
                        color: 'text.primary',
                    },
                }}
            >
                <DataGrid
                    components={{
                        LoadingOverlay: CustomLoadingOverlay,
                        Pagination: CustomPagination
                    }}

                    rows={rows}
                    columns={columns}
                    pageSize={8}
                    rowsPerPageOptions={[6]}
                    loading={loading}

                // checkboxSelection
                />
            </Box>
        </>

    );
}


